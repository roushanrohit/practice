package trees;

public class Quadruplet {

    int min;
    int max;
    boolean isBST;
    int heightOfLargestBST;

    public Quadruplet(int min, int max, boolean isBST, int heightOfLargestBST) {
        this.min = min;
        this.max = max;
        this.isBST = isBST;
        this.heightOfLargestBST = heightOfLargestBST;
    }
}
